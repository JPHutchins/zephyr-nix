#!/usr/bin/env bash
set -euo pipefail

echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo "Checking zephyr-nix flake..."
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

echo "1. Checking flake structure..."
nix flake show

echo ""
echo "2. Checking flake metadata..."
nix flake metadata

echo ""
echo "3. Evaluating packages..."
nix eval .#packages.x86_64-linux --apply builtins.attrNames

